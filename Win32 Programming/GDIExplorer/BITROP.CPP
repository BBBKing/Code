/****************************************************************************
*                                 CBitRop::Load
* Inputs:
*       BOOL ext: TRUE to use extended opcodes, FALSE for normal opcodes
*       int def: Default value
* Result: int
*       Selected object
* Effect: 
*       Loads the combo box and makes the initial selection
****************************************************************************/

int CBitRop::Load(BOOL ext, int def)
    {
     ResetContent();
     return CIDCombo::AddStrings((ext ? extrops : rops), def);
    }
